a = 12 # Assigment Operator
b = 5

# Arithmetic Operators
print(a + b) # Addition
print(a - b) # Subtraction
print(a * b) # Multiplication
print(a / b) # Division : always gives answer in float for true division
print(a // b) # Integer Division : gives the whole part
print(a % b) # Modulo
print(a ** b) # Exponentiation

# Compound Assigment Operators
a += 8 # a = a + 8
b -= 3
print(a, b)
a **= b
b /= a
print(a, b)

c = 5
d = 3
print(c, d)
c = c + d # Swapping two var with only operations
d = c - d
c = c - d
print(c, d)

# Relational Operators
a = 35
b = 40
a > b # Greater than
a < b # Smaller than
a == b # Equal to
a != b # Not Equal to
a >= b # Greater OR Equal to
a <= b # Smaller OR Equal to

